// В цьому регулярньму виразі перевіряємо присутність Аа (англ) Аа (укр, рос)
const regex = /^(?!.*[AaАа]).{1,255}$/;

// Тут вводимо певний тест для пошуку літер Аа:
const testString = "Your test string with Cyrillic";

// Перевіряємо чи відповідає текст критеріям регулярного виразу:
if (regex.test(testString)) {
    console.log("Рядок відповідає критеріям.");
} else {
    console.log("Рядок не відповідає критеріям.");
}
